package Condicoes;

public class loops {
    static void main(String[] args) {

        int x = 0;
        int y = 5;

        while (x < y) {
            System.out.println("Repetiu " + x + " vezes");
            x++;
        }


        for (int i = 0; i < 10; i++) {
            System.out.println("Repetiu " + i + " vezes");
        }
    }
}
